SKIP_FILES = {'__init__.py', 'deep_audit.py', 'sync_translations.py'}

for fname in sorted(os.listdir(BASE_DIR)):
    if not fname.endswith('.py') or fname in SKIP_FILES:
        continue
    
    lang = fname[:-3]  # remove .py
    filepath = os.path.join(BASE_DIR, fname)
    
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    if 'digest_title' in content:
        print(f"SKIP: {lang} already has digest keys")
        continue
    
    # Merge: EN defaults + language overrides
    keys = dict(EN_KEYS)
    if lang in LANG_OVERRIDES:
        keys.update(LANG_OVERRIDES[lang])
    
    # Find the last } in the file
    last_brace = content.rfind('}')
    if last_brace == -1:
        print(f"ERROR: no closing brace in {fname}")
        continue
    
    # Build keys block
    block = "\n    # Daily Digest\n"
    for k, v in keys.items():
        v_escaped = v.replace("'", "\\'")
        block += f"    '{k}': '{v_escaped}',\n"
    
    new_content = content[:last_brace] + block + content[last_brace:]
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(new_content)
    
    print(f"OK: {lang} - added {len(keys)} digest keys")

print("\nDone!")
